package org.amityregion5.qxrz.client.ui.screen;

public class WindowData
{
	private int width;
	private int height;
	
	/**
	 * @param width
	 * @param height
	 */
	public WindowData(int width, int height)
	{
		this.width = width;
		this.height = height;
	}
	/**
	 * @return the width
	 */
	public int getWidth()
	{
		return width;
	}
	/**
	 * @param width the width to set
	 */
	public void setWidth(int width)
	{
		this.width = width;
	}
	/**
	 * @return the height
	 */
	public int getHeight()
	{
		return height;
	}
	/**
	 * @param height the height to set
	 */
	public void setHeight(int height)
	{
		this.height = height;
	}
	
	
}
